export function useProducts(): Product[] {
  const { t } = useTranslation("data");
  return productsBase.map((p) => ({
    ...p,
    title: t(`products.${p.id}.title`),
    shortDescription: t(`products.${p.id}.shortDescription`),
    description: t(`products.${p.id}.description`),
    specifications: t(`products.${p.id}.specifications`, { returnObjects: true }) as {
      label: string;
      value: string;
    }[],
  }));
}

export const getProductById = (id: number) => productsBase.find((p) => p.id === id);
