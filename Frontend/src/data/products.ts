export const getProductById = (id: number) => products.find((p) => p.id === id);
export const getProductBySlug = (slug: string) => products.find((p) => p.slug === slug);
export const getProductsByCategory = (categoryId: number) => products.filter((p) => p.categoryId === categoryId);
export const getProductsByFarmer = (farmerId: number) => products.filter((p) => p.farmerId === farmerId);
export const getRelatedProducts = (product: Product, limit = 4) =>
  products
    .filter((p) => p.id !== product.id && (p.categoryId === product.categoryId || p.farmerId === product.farmerId))
    .slice(0, limit);
